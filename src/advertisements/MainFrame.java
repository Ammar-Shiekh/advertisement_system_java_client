    public MainFrame() throws Exception {
        
        Authentication auth = Helper.getAuthentication();
        ads = new HashMap();
        Advertisement[] adsArray = Helper.getAdvertisements(auth);
        
        for (Advertisement ad : adsArray) {
            ads.put(ad.getId(), ad);
        }
        
        ServerAdapter.Initialize(auth, 
                // advertisement added
                ad -> {
                    ads.put(ad.getId(), ad);
                }, 
                // advertisement removed
                ad -> {
                    ads.remove(ad.getId());
                }, 
                // advertisement updated
                ad -> {
                    ads.put(ad.getId(), ad);
                });
        initComponents();
    }
    
