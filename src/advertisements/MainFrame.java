    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        displayingPanel.setPreferredSize(new Dimension(getWidth(), getHeight()));
        displayer.setPreferredSize(new Dimension(displayingPanel.getWidth(), displayingPanel.getHeight()));
        displayingThread = new Thread(() -> {
            Iterator<Entry<Integer, Advertisement>> i = ads.entrySet().iterator();
            while(true) {
                try {
                    if (ads.isEmpty()) {
                        TimeUnit.SECONDS.sleep(5);
                    } else {
                        try {
                            if (!i.hasNext()) i = ads.entrySet().iterator();
                            Advertisement ad = i.next().getValue();
                            URL url = new URL(ad.getImageUrl());
                            Image image = ImageIO.read(url);
                            displayer.setIcon(new ImageIcon(image.getScaledInstance(displayer.getWidth(), 
                                    displayer.getHeight(), Image.SCALE_SMOOTH)));
                            TimeUnit.SECONDS.sleep(ad.getDuration());
                        } catch (IOException e) {

                        }
                    }
                } catch(InterruptedException e) {
                    
                }
            }
        });
        displayingThread.start();
    }//GEN-LAST:event_formWindowOpened

